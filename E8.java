package JavaP;

public class E8 {
    public static void main(String[] args) {
        String [] s={"Nasima","Asghar","nargas","Arsh","jahn"};
        for (String r:s){
            if (r.startsWith("A")){
                System.out.println(r.toLowerCase());
            }
        }

    }
}
